    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        try {
            koneksi();
            int row =jTable1.getSelectedRow();
            String tabel_klik=(jTable1.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = con.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from identitas where id='"+tabel_klik+"'");
            if(sql.next()){
                String id = sql.getString("id");
                jTextField1.setText(id);
                String nama = sql.getString("nama");
                jTextField2.setText(nama);
                String jenis_kelamin = sql.getString("jenis_kelamin");
                jTextField3.setText(jenis_kelamin);
                String agama = sql.getString("agama");
                jTextField4.setText(agama);
                String alamat = sql.getString("alamat");
                jTextField5.setText(alamat);
            }
        } catch (Exception e) {}
    }//GEN-LAST:event_jTable1MouseClicked

